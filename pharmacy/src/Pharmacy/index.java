    public int testFacture() {
        String Query = "select max(`nofacture`) as 'max_id' from drug";
        try {
            ps = connection.getConnection().prepareStatement(Query);
            rs = ps.executeQuery();
            if (rs.next()) {
                int max_id = rs.getInt("max_id");
                max_id += 1;
                return 1;
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);

        }
        return 1;
    }

    public ArrayList<firstTabledata> getStockInfo() {
        ArrayList<firstTabledata> stockList = new ArrayList<>();
        Connection conn = connection.getConnection();
        String FacNo = fn.getText();
        if (!FacNo.isEmpty()) {
            String query = "select * from drug where nofacture = " + FacNo;
            Statement st;
            ResultSet rs;
            try {
                st = conn.createStatement();
                rs = st.executeQuery(query);
                firstTabledata ftd;
                while (rs.next()) {
                    ftd = new firstTabledata(rs.getInt("id"), rs.getString("name"), rs.getString("nlot"), rs.getString("fullexp"), rs.getString("quantity"), rs.getString("price"), rs.getString("montant"));
                    stockList.add(ftd);
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }

        } else {
            // PASS
        }
        return stockList;
    }

