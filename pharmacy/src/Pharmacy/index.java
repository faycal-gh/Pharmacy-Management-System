    public String restStock(String name) {
        Connection connection = getConnection();
        String query = "select rstocke from `medicaments` where name='" + name + "'";
        Statement st;
        ResultSet rs;
        String reset = null;
        try {
            st = connection.createStatement();
            rs = st.executeQuery(query);
            while (rs.next()) {
                reset = rs.getString("rstocke");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
        return reset;
    }

