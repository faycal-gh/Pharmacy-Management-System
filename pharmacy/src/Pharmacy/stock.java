    public void search(String q) {
        DefaultTableModel dtm = (DefaultTableModel) tblinfo.getModel();
        TableRowSorter<DefaultTableModel> trs = new TableRowSorter<DefaultTableModel>(dtm);
        tblinfo.setRowSorter(trs);
        trs.setRowFilter(RowFilter.regexFilter(q));
    }
