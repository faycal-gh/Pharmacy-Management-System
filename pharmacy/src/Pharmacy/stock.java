    public ArrayList<stockIinfo> getStockInfo() {
        ArrayList<stockIinfo> stockList = new ArrayList<>();
        Connection conn = connection.getConnection();
        String query = "select * from medicaments";
        Statement st;
        ResultSet rs;
        try {
            st = conn.createStatement();
            rs = st.executeQuery(query);
            stockIinfo sti;
            while (rs.next()) {
                sti = new stockIinfo(rs.getString("name"), rs.getString("quantity"), rs.getString("nlot"), rs.getString("price"), rs.getString("fullexp"));
                stockList.add(sti);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
        return stockList;
    }

    public void show_information_in_JTable() {
        ArrayList<stockIinfo> list = getStockInfo();
        DefaultTableModel f = (DefaultTableModel) tblinfo.getModel();
        Object obj[] = new Object[5];
        for (int x = 0; x < list.size(); x++) {
            obj[0] = list.get(x).getNom();
            obj[1] = list.get(x).getQuantity();
            obj[2] = list.get(x).getNlot();
            obj[3] = list.get(x).getPrix();
            obj[4] = list.get(x).getExpdate();
            f.addRow(obj);
        }
    }

